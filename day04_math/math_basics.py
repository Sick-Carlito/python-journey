# Day 4 - Python Math & Operators

# Basic math
a = 10
b = 3


print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)
print("a % b =", a % b)
print("a ** b =", a ** b)

# Order of operations
result = 10 + 5 * 2
print("10 + 5 * 2 =", result)  # Output: 20, not 30!

# Grouping with parentheses
result2 = (10 + 5) * 2
print("(10 + 5) * 2 =", result2)


